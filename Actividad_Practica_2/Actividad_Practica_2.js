// Crea un algoritmo que use un ciclo for y while

// While: Un ciclo while se ejecuta mientras una condicion sea verdadera.
let contador = 0;
while (contador<11) {
    console.log("Contador while: " + contador);
    contador++;
}

// For: Un ciclo for se ejecuta un numero determinado de veces fijado por el programador.
for (let i = 0; i<11; i++) {
    console.log("Contador for: " + i);
}