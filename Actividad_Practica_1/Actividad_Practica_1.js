// Tipo de Variables

// String: Un string es una cadena de caracteres, es decir, un texto. Se define entre comillas simples (' ') o dobles (" ").
let nombre = "Alejandro";
let apellido = "Almaguer";
let nombreCompleto = nombre + " " + apellido;
console.log("Mi nombre es " + nombreCompleto);

// Number: Un number es un valor numerico, ya sea entero o decimal.
let edad = 24;
let peso = 57.80;
let altura =1.63; // en metros
let BMI = peso / (altura * altura);
console.log("Mi BMI es " + BMI);

//Boolean: Un boolean es un valor logico que puede ser verdadero (true) o falso (false).
let estudiante = false;
let trabajador = true;
let fracasado = true;
console.log("Soy estudiante: " + estudiante);

